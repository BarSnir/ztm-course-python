selfish = "me me me me me me"
print(selfish[0])
print(selfish[7])
print(selfish[0:7:2])
# start, stop, step
print(selfish[1:])
# To the end
print(selfish[:5])
# To concrete index
print(selfish[::2])
# Step until the end
print(selfish[-1])
# From the end
print(selfish[::-1])
# step by -1, reversing order
print(selfish[::-2])
# step by -2, reversing order
print(selfish)